u_int8 uicom_font_getdata(u_int8 *ch, u_int8 chsize ,struct UICOM_1PP_FONT_INFO *info, T_UICOM_FONT_SIZE fontsize)
{
    u_int32 idx;
    u_int8  val;

    //MY_DEBUG(":[%2X,%2X]\n",ch[0],ch[1]);  
    //CKJ
    if (UTIL_STR_MBCS_LEN == chsize)
    {
        for (idx=0; idx < FONT12_CKJ_COUNT; idx++)
        {
            if ((ch[0] == gFont12_CkjMapInfo[idx].code[0]) && (ch[1] == gFont12_CkjMapInfo[idx].code[1]))
            {
                //MY_DEBUG("\tCJK[%2X,%2X]\n", gFont12_CkjMapInfo[idx].code[0], gFont12_CkjMapInfo[idx].code[1]);  
                info->data   = (u_int8 *)&(gFont12_CkjMapInfo[idx].data[1]);
                info->bottom = FONT12CJK_BOTTOM_OFS(gFont12_CkjMapInfo[idx].data[0]);
                info->right  = FONT12CJK_RIGHT_OFS(gFont12_CkjMapInfo[idx].data[0]);
                info->left   = FONT12CJK_LEFT_OFS(gFont12_CkjMapInfo[idx].data[0]);
                info->width  = 12;
                info->height = 12;
                return info->width;
            }
        }
    }
    else if (1 == chsize)
    {
        val = ch[0];
        if (fontsize == FONT_SIZE_NORMAL)
        {
            if (val < 32)
                val = 32;
            
            idx = val - 32;
            if (idx < FONT12_CKJ_COUNT)
            {
                //MY_DEBUG("Small:[%d/%d:%d]\n",idx, ARRAY_SIZE(gFont6_AsciiMap), FONT6_ASCII_COUNT);  
                info->data = (u_int8 *)&gFont12_AsciiMap[idx][1];
                info->bottom = 0;
                info->right  = 1;
                info->left   = 0;
                info->width  = (0 == idx)?3:6;// if is space char, just 3 pixel
                info->height = 12;
                return info->width;
            }
        }
        else// FONT_SIZE_SMALL
        {
#ifdef FONT6_ASCII_FULL_LIB
#else
            if (val>='a')
            {
                if ( val<='z')
                {
                    val = 'A' + val - 'a';
                }
                else
                {
                    val = 32;
                }
            }
#endif
            if (val < 32)
                val = 32;
            idx = val - 32;
            if (idx < FONT6_ASCII_COUNT)
            {
                //MY_DEBUG("Small:[%d/%d:%d]\n",idx, ARRAY_SIZE(gFont6_AsciiMap), FONT6_ASCII_COUNT);  
                info->data = (u_int8 *)&gFont6_AsciiMap[idx];
                info->bottom = 0;
                info->right  = 1;
                info->left   = 0;
                info->width  = (0 == idx)?2:6;
                info->height = 8;
                return info->width;
            }
        }
    }

    return 0;
}

